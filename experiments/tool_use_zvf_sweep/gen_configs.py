#!/usr/bin/env python3
"""
Generate 12 sweep configs: (Qwen2.5-0.5B, Qwen3-8B) x (v1, v2 reward) x (seed 42,43,44).
Run: .venv/bin/python experiments/tool_use_zvf_sweep/gen_configs.py
"""
from pathlib import Path
import textwrap

HERE = Path(__file__).resolve().parent
OUT = HERE / "configs"
OUT.mkdir(parents=True, exist_ok=True)

MODELS = {
    "qwen3_4b": {
        "tokenizer": "Qwen/Qwen3-4B-Instruct-2507",
        "tinker_model_name": "Qwen/Qwen3-4B-Instruct-2507",
        "batch_size": 64,
        "max_num_workers": 16,
        "num_requests_for_eval": 100,
        "learning_rate": 5e-5,
    },
    "qwen3_8b": {
        "tokenizer": "Qwen/Qwen3-8B",
        "tinker_model_name": "Qwen/Qwen3-8B",
        "batch_size": 128,
        "max_num_workers": 24,
        "num_requests_for_eval": 200,
        "learning_rate": 4e-5,
    },
}
REWARDS = ["v1", "v2"]
SEEDS = [42, 43, 44]

TEMPLATE = """\
# auto-generated by gen_configs.py; do not hand-edit
# model={model} reward={reward} seed={seed}
env:
  group_size: 16
  batch_size: {batch_size}
  max_batches_offpolicy: 3
  tokenizer_name: "{tokenizer}"
  use_wandb: true
  rollout_server_url: "http://localhost:18000"
  wandb_name: "tool-use-{model}-{reward}-s{seed}-env"
  ensure_scores_are_not_same: false
  max_token_length: 512
  max_num_workers: {max_num_workers}
  total_steps: 50
  steps_per_eval: 10
  data_seed: {seed}

openai:
  - model_name: "{tokenizer}"
    base_url: "http://localhost:8001/v1"
    api_key: "x"
    weight: 1.0
    num_requests_for_eval: {num_requests_for_eval}

tinker:
  lora_rank: 32
  learning_rate: {learning_rate}
  max_token_trainer_length: 1024
  checkpoint_dir: "./checkpoints/tool_use_zvf_sweep/{model}_{reward}_s{seed}/"
  save_checkpoint_interval: 10
  tinker_model_name: "{tinker_model_name}"
  wandb_project: "tinker-rl-zvf-counterfactual"
  wandb_group: "tool-use-zvf-sweep"
  wandb_run_name: "tool-use-{model}-{reward}-s{seed}"

slurm: false
testing: false
"""


def main() -> None:
    manifest: list[str] = []
    for model_key, spec in MODELS.items():
        for reward in REWARDS:
            for seed in SEEDS:
                name = f"tool_use_{model_key}_{reward}_s{seed}.yaml"
                path = OUT / name
                path.write_text(
                    TEMPLATE.format(
                        model=model_key,
                        reward=reward,
                        seed=seed,
                        **spec,
                    )
                )
                manifest.append(f"{name}\t{reward}\t{seed}\t{spec['tokenizer']}")

    (HERE / "manifest.tsv").write_text(
        "config\treward\tseed\ttokenizer\n" + "\n".join(manifest) + "\n"
    )
    print(f"Wrote {len(manifest)} configs to {OUT}")
    print(f"Manifest: {HERE / 'manifest.tsv'}")


if __name__ == "__main__":
    main()
